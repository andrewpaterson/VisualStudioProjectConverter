#include "FilterFiles.h"


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CFilterFiles::Init(void)
{
	mcGuid.Clear();
	mcFiles.Init();
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CFilterFiles::Kill(void)
{
	mcFiles.Kill();
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CFilterFiles::AddFile(char* szFilename)
{
	mcFiles.Add(szFilename);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
TRISTATE CFilterFiles::SetGuid(char* szGuid)
{
	return mcGuid.Init(szGuid);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
CArrayChars* CFilterFiles::GetFiles(void)
{
	return &mcFiles;
}

