#include "FileFilters.h"


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CFileFilters::Init(char* szFilename)
{
	mcFilters.Init();
	mszFilename.Init(szFilename);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CFileFilters::Kill(void)
{
	mszFilename.Kill();
	mcFilters.Kill();
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CFileFilters::AddFilter(char* szName)
{
	mcFilters.Add(szName);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
CArrayChars* CFileFilters::GetFilters(void)
{
	return &mcFilters;
}

